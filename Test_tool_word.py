import nltk
from nltk.corpus import wordnet
from googletrans import Translator

# Tải các tài nguyên cần thiết của NLTK
nltk.download('wordnet')
nltk.download('omw-1.4')

# Hàm để dịch từ sang tiếng Anh (hoặc ngôn ngữ khác nếu cần)
def translate_to_english(word):
    translator = Translator()
    translation = translator.translate(word, dest='en')
    return translation.text.lower()

# Hàm để tìm các từ đồng nghĩa
def get_synonyms(word):
    synonyms = set()
    for syn in wordnet.synsets(word):
        for lemma in syn.lemmas():
            synonyms.add(lemma.name().lower())
    return synonyms

# Hàm chính để lọc từ
def filter_similar_words(words):
    translated_words = {word: translate_to_english(word) for word in words}
    similar_groups = []
    
    while translated_words:
        word, translated_word = translated_words.popitem()
        synonyms = get_synonyms(translated_word)
        
        similar_group = [word] + [w for w, tw in translated_words.items() if tw in synonyms]
        translated_words = {w: tw for w, tw in translated_words.items() if tw not in synonyms}
        
        similar_groups.append(similar_group)
    
    return similar_groups

# Hàm để nhập danh sách từ từ bàn phím
def input_words():
    words = []
    print("Nhập các từ (nhấn Enter để kết thúc):")
    while True:
        word = input("Nhập từ: ")
        if word == "":
            break
        words.append(word)
    return words

# Hàm để tìm các từ đồng nghĩa của một từ cụ thể
def find_synonyms_for_word(word):
    translated_word = translate_to_english(word)
    synonyms = get_synonyms(translated_word)
    return synonyms

# Nhập từ để tìm các từ đồng nghĩa
target_word = input("Nhập từ bạn muốn tìm các từ đồng nghĩa: ")
synonyms = find_synonyms_for_word(target_word)
print(f"Các từ đồng nghĩa với '{target_word}': {', '.join(synonyms)}")

